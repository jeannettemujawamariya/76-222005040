
//java program to declare switch case method
class worknine{
    public static void main(String[]args){
        int day=1;
        switch(day){
            case 2:
                System.out.println("today is on tuesday");
                break;
                case 3:
                    System.out.println("today is on wednesday");
                    break;
                    case 4:
                        System.out.println("today is on thursday");
                        default:
                            System.out.println("looking forward to the next day");
        }
    }
}